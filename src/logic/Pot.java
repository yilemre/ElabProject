package logic; 
public class Pot {
    
    private String name;
    private double targetStock;
    private double actualStock;
    
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public double getTargetStock() {
        return targetStock;
    }
    public void setTargetStock(double targetStock) {
        this.targetStock = targetStock;
    }
    public double getActualStock() {
        return actualStock;
    }
    public void setActualStock(double actualStock) {
        this.actualStock = actualStock;
    }
    
    
	
}
